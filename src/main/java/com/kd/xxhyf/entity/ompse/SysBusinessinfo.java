package com.kd.xxhyf.entity.ompse;


public class SysBusinessinfo {

  private String id;
  private String businessName;
  private String fatherId;
  private String tableidRule;
  private String tablenameRule;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getBusinessName() {
    return businessName;
  }

  public void setBusinessName(String businessName) {
    this.businessName = businessName;
  }


  public String getFatherId() {
    return fatherId;
  }

  public void setFatherId(String fatherId) {
    this.fatherId = fatherId;
  }


  public String getTableidRule() {
    return tableidRule;
  }

  public void setTableidRule(String tableidRule) {
    this.tableidRule = tableidRule;
  }


  public String getTablenameRule() {
    return tablenameRule;
  }

  public void setTablenameRule(String tablenameRule) {
    this.tablenameRule = tablenameRule;
  }

}
