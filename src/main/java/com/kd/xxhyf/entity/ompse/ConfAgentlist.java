package com.kd.xxhyf.entity.ompse;


public class ConfAgentlist {

  private String id;
  private String remark;
  private String ip;
  private String port;
  private String securityarea;
  private long pingport;
  private String dataNetworkIp;
  private String groupid;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getRemark() {
    return remark;
  }

  public void setRemark(String remark) {
    this.remark = remark;
  }


  public String getIp() {
    return ip;
  }

  public void setIp(String ip) {
    this.ip = ip;
  }


  public String getPort() {
    return port;
  }

  public void setPort(String port) {
    this.port = port;
  }


  public String getSecurityarea() {
    return securityarea;
  }

  public void setSecurityarea(String securityarea) {
    this.securityarea = securityarea;
  }


  public long getPingport() {
    return pingport;
  }

  public void setPingport(long pingport) {
    this.pingport = pingport;
  }


  public String getDataNetworkIp() {
    return dataNetworkIp;
  }

  public void setDataNetworkIp(String dataNetworkIp) {
    this.dataNetworkIp = dataNetworkIp;
  }


  public String getGroupid() {
    return groupid;
  }

  public void setGroupid(String groupid) {
    this.groupid = groupid;
  }

}
