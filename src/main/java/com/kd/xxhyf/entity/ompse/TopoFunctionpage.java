package com.kd.xxhyf.entity.ompse;


public class TopoFunctionpage {

  private String id;
  private String functionPage;
  private String centerX;
  private String centerY;
  private String dccId;
  private String scale;
  private String desc;
  private String systemId;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getFunctionPage() {
    return functionPage;
  }

  public void setFunctionPage(String functionPage) {
    this.functionPage = functionPage;
  }


  public String getCenterX() {
    return centerX;
  }

  public void setCenterX(String centerX) {
    this.centerX = centerX;
  }


  public String getCenterY() {
    return centerY;
  }

  public void setCenterY(String centerY) {
    this.centerY = centerY;
  }


  public String getDccId() {
    return dccId;
  }

  public void setDccId(String dccId) {
    this.dccId = dccId;
  }


  public String getScale() {
    return scale;
  }

  public void setScale(String scale) {
    this.scale = scale;
  }


  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }


  public String getSystemId() {
    return systemId;
  }

  public void setSystemId(String systemId) {
    this.systemId = systemId;
  }

}
