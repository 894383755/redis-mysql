package com.kd.xxhyf.entity.ompse;


public class SysWebpageedit {

  private long id;
  private String webpageName;
  private String webpageCode;
  private String webpageDesc;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getWebpageName() {
    return webpageName;
  }

  public void setWebpageName(String webpageName) {
    this.webpageName = webpageName;
  }


  public String getWebpageCode() {
    return webpageCode;
  }

  public void setWebpageCode(String webpageCode) {
    this.webpageCode = webpageCode;
  }


  public String getWebpageDesc() {
    return webpageDesc;
  }

  public void setWebpageDesc(String webpageDesc) {
    this.webpageDesc = webpageDesc;
  }

}
