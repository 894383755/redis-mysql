package com.kd.xxhyf.entity.ompse;


public class ConfDeviceproducerinfo {

  private String id;
  private String platformtype;
  private String deviceproducer;
  private String devicemodel;
  private String versionnumber;
  private String devdllname;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getPlatformtype() {
    return platformtype;
  }

  public void setPlatformtype(String platformtype) {
    this.platformtype = platformtype;
  }


  public String getDeviceproducer() {
    return deviceproducer;
  }

  public void setDeviceproducer(String deviceproducer) {
    this.deviceproducer = deviceproducer;
  }


  public String getDevicemodel() {
    return devicemodel;
  }

  public void setDevicemodel(String devicemodel) {
    this.devicemodel = devicemodel;
  }


  public String getVersionnumber() {
    return versionnumber;
  }

  public void setVersionnumber(String versionnumber) {
    this.versionnumber = versionnumber;
  }


  public String getDevdllname() {
    return devdllname;
  }

  public void setDevdllname(String devdllname) {
    this.devdllname = devdllname;
  }

}
