package com.kd.xxhyf.entity.ompse;


public class SysTableinfo {

  private String id;
  private String chnTablename;
  private String enTablename;
  private String desc;
  private String businessId;
  private String isWhole;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getChnTablename() {
    return chnTablename;
  }

  public void setChnTablename(String chnTablename) {
    this.chnTablename = chnTablename;
  }


  public String getEnTablename() {
    return enTablename;
  }

  public void setEnTablename(String enTablename) {
    this.enTablename = enTablename;
  }


  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }


  public String getBusinessId() {
    return businessId;
  }

  public void setBusinessId(String businessId) {
    this.businessId = businessId;
  }


  public String getIsWhole() {
    return isWhole;
  }

  public void setIsWhole(String isWhole) {
    this.isWhole = isWhole;
  }

}
