package com.kd.xxhyf.entity.ompse;


public class MessageWarning {

  private double unid;
  private String clientlable;
  private String id;
  private String ip;
  private String warninglevel;
  private String devicetype;
  private String wmac;
  private String starttime;
  private String endtime;
  private String content;


  public double getUnid() {
    return unid;
  }

  public void setUnid(double unid) {
    this.unid = unid;
  }


  public String getClientlable() {
    return clientlable;
  }

  public void setClientlable(String clientlable) {
    this.clientlable = clientlable;
  }


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getIp() {
    return ip;
  }

  public void setIp(String ip) {
    this.ip = ip;
  }


  public String getWarninglevel() {
    return warninglevel;
  }

  public void setWarninglevel(String warninglevel) {
    this.warninglevel = warninglevel;
  }


  public String getDevicetype() {
    return devicetype;
  }

  public void setDevicetype(String devicetype) {
    this.devicetype = devicetype;
  }


  public String getWmac() {
    return wmac;
  }

  public void setWmac(String wmac) {
    this.wmac = wmac;
  }


  public String getStarttime() {
    return starttime;
  }

  public void setStarttime(String starttime) {
    this.starttime = starttime;
  }


  public String getEndtime() {
    return endtime;
  }

  public void setEndtime(String endtime) {
    this.endtime = endtime;
  }


  public String getContent() {
    return content;
  }

  public void setContent(String content) {
    this.content = content;
  }

}
