package com.kd.xxhyf.entity.ompse;


public class SysControls {

  private String id;
  private String controlsnameEn;
  private String controlsnameCh;
  private String controlsdesc;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getControlsnameEn() {
    return controlsnameEn;
  }

  public void setControlsnameEn(String controlsnameEn) {
    this.controlsnameEn = controlsnameEn;
  }


  public String getControlsnameCh() {
    return controlsnameCh;
  }

  public void setControlsnameCh(String controlsnameCh) {
    this.controlsnameCh = controlsnameCh;
  }


  public String getControlsdesc() {
    return controlsdesc;
  }

  public void setControlsdesc(String controlsdesc) {
    this.controlsdesc = controlsdesc;
  }

}
