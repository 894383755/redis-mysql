package com.kd.xxhyf.entity.ompse;


public class TopoEntityType {

  private String id;
  private String entityTypename;
  private String tableId;
  private String runningInfoUrl;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getEntityTypename() {
    return entityTypename;
  }

  public void setEntityTypename(String entityTypename) {
    this.entityTypename = entityTypename;
  }


  public String getTableId() {
    return tableId;
  }

  public void setTableId(String tableId) {
    this.tableId = tableId;
  }


  public String getRunningInfoUrl() {
    return runningInfoUrl;
  }

  public void setRunningInfoUrl(String runningInfoUrl) {
    this.runningInfoUrl = runningInfoUrl;
  }

}
