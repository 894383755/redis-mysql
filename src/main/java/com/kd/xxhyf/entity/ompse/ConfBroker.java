package com.kd.xxhyf.entity.ompse;


public class ConfBroker {

  private String id;
  private String ip;
  private long flag;
  private long security;
  private String desc;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getIp() {
    return ip;
  }

  public void setIp(String ip) {
    this.ip = ip;
  }


  public long getFlag() {
    return flag;
  }

  public void setFlag(long flag) {
    this.flag = flag;
  }


  public long getSecurity() {
    return security;
  }

  public void setSecurity(long security) {
    this.security = security;
  }


  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }

}
