public class DcdVersioncheck {

  private String id;
  private String dcdmodel;
  private String dcdversion;
  private String matchstate;
  private String checkstatenum;
  private String lastchecktime;


  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }


  public String getDcdmodel() {
    return dcdmodel;
  }

  public void setDcdmodel(String dcdmodel) {
    this.dcdmodel = dcdmodel;
  }


  public String getDcdversion() {
    return dcdversion;
  }

  public void setDcdversion(String dcdversion) {
    this.dcdversion = dcdversion;
  }


  public String getMatchstate() {
    return matchstate;
  }

  public void setMatchstate(String matchstate) {
    this.matchstate = matchstate;
  }


  public String getCheckstatenum() {
    return checkstatenum;
  }

  public void setCheckstatenum(String checkstatenum) {
    this.checkstatenum = checkstatenum;
  }


  public String getLastchecktime() {
    return lastchecktime;
  }

  public void setLastchecktime(String lastchecktime) {
    this.lastchecktime = lastchecktime;
  }

}
