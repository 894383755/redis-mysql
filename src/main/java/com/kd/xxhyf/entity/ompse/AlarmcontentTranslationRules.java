package com.kd.xxhyf.entity.ompse;


public class AlarmcontentTranslationRules {

  private long indexid;
  private String alarcontentEn;
  private String alarcontentCn;
  private String remark;


  public long getIndexid() {
    return indexid;
  }

  public void setIndexid(long indexid) {
    this.indexid = indexid;
  }


  public String getAlarcontentEn() {
    return alarcontentEn;
  }

  public void setAlarcontentEn(String alarcontentEn) {
    this.alarcontentEn = alarcontentEn;
  }


  public String getAlarcontentCn() {
    return alarcontentCn;
  }

  public void setAlarcontentCn(String alarcontentCn) {
    this.alarcontentCn = alarcontentCn;
  }


  public String getRemark() {
    return remark;
  }

  public void setRemark(String remark) {
    this.remark = remark;
  }

}
