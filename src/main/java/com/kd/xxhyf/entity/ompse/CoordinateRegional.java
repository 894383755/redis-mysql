package com.kd.xxhyf.entity.ompse;


public class CoordinateRegional {

  private String regional;
  private String xregional;
  private String yregional;
  private String regionalDec;


  public String getRegional() {
    return regional;
  }

  public void setRegional(String regional) {
    this.regional = regional;
  }


  public String getXregional() {
    return xregional;
  }

  public void setXregional(String xregional) {
    this.xregional = xregional;
  }


  public String getYregional() {
    return yregional;
  }

  public void setYregional(String yregional) {
    this.yregional = yregional;
  }


  public String getRegionalDec() {
    return regionalDec;
  }

  public void setRegionalDec(String regionalDec) {
    this.regionalDec = regionalDec;
  }

}
